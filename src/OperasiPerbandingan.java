package src;

public class OperasiPerbandingan {
    public static void main(String[] args) {
        int value1 = 100;
        int value2 = 200;

        System.out.println(value1 > value2);
        System.out.println(value1 < value2);
        System.out.println(value2 >= value1);
        System.out.println(value2 <= value1);
        System.out.println(value2 == value2);
        System.out.println(value2 != value1);
    }
}
